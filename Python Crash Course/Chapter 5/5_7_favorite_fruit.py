# 5-7. Favorite Fruit: Make a list of your favorite fruits,
# and then write a series of independent if statements that
# check for certain fruits in your list.
# 1) Make a list of your three favorite fruits and call it
# favorite_fruits.

favorite_fruits = ["Grapes", "Mango", "Watermelon"]

# 2) Write five if statements. Each should check whether a
# certain kind of fruit is in your list. If the fruit is in
# your list, the if block should print a statement, such as
# You really like bananas!

if "Grapes" in favorite_fruits:
    print("You really like grapes!")
if "Banana" in favorite_fruits:
    print("You really like Banana's!")
if "Mango" in favorite_fruits:
    print("You really like Mango's!")
if "Apple" in favorite_fruits:
    print("You really like Apples!")
if "Watermelon" in favorite_fruits:
    print("You really like Watermelons!")